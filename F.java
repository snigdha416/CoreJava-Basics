class F 
{
	/*
	
	Literals
	-----------
	String    ==>   "abc", "345", "false", "@#$%", "", "  ", "R", "56.09"
	int       ==>   10, 0, -14, 90, -2637
	Double    ==>   0.0, 3.6, 435.76, 51.06, 5.0, -134.08, -46.09
	char      ==>   'e', '7', 'r', '$', '@', '*', '0', 'D'
	Boolean   ==>   true, false
 	
   */
	public static void main(String[] args) 
	{
		System.out.println("Hello World!"); // string literal
		System.out.println(10);   // int literal
		System.out.println(1.9);  // double literal
		System.out.println('f');  // char literal
		System.out.println(true); // boolean literal
		
	}
}
